package java03;

public class J_01_Arithmetic {

    public static void main(String[] args) {
        int number1 = 13;
        int number2 = 10;

        // Addition
        System.out.println(number1 + number2);

        // Subtraction
        System.out.println(number1 - number2);

        // Multiplication
        System.out.println(number1 * number2);

        // Division
        System.out.println(number1 / number2);

        // Modulus
        System.out.println(number1 % number2);
    }

}
