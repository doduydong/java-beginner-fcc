package java03;

public class J_02_Assignment {

    public static void main(String[] args) {
        int number = 10;

        System.out.println(number += 7); // number = number + 7
        System.out.println(number -= 6); // number = number - 6
        System.out.println(number *= 10); // number = number * 10
        System.out.println(number /= 10); // number = number / 10
        System.out.println(number %= 10); // number = number / 10
    }

}
