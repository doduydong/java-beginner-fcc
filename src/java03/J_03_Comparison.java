package java03;

public class J_03_Comparison {

    public static void main(String[] args) {
        int number1 = 13;
        int number2 = 10;

        // Equal to
        System.out.println(number1 == number2);

        // Not equal
        System.out.println(number1 != number2);

        // Greater than
        System.out.println(number1 > number2);

        // Less than
        System.out.println(number1 < number2);

        // Greater than or equal to
        System.out.println(number1 >= number2);

        // Less than or equal to
        System.out.println(number1 <= number2);
    }

}
